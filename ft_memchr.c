void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*i;

	i = s;
	while (*i != c && *i != '\0' && n > 0)
	{
		i++;
		n--;
	}
	if (n == 0)
		return (0);
	return ((void *) i);
}
