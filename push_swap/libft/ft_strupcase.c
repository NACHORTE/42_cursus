char	*ft_strupcase(char *str)
{
	int	aux;

	aux = 0;
	while (str[aux] != '\0')
	{
		if (str[aux] >= 'a' && str[aux] <= 'z')
		str[aux] = (str[aux] - 'a') + 'A';
		aux++;
	}
	return (str);
}
