char	*ft_strrchr(const char *s, int c)
{
	char	*r;

	r = 0;
	while (*s != '\0')
	{
		if (*s == (char) c)
			r = (char *) s;
		s++;
	}
	if ((char) c == '\0')
		return ((char *) s);
	else
		return (r);
}
